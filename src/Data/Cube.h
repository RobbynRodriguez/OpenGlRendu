using namespace glm;
using namespace std;

class Cube{

public:

    Mesh mesh;

    Cube(){
        vector<Vertex> vertices;
        vector<unsigned int> indices;
        vector<Texture> textures;




        for(int i = 0 ; i<288; i+=8){
            Vertex vertex(
                    vec3(v[i],v[i+1],v[i+2]),
                    vec3(v[i+3],v[i+4],v[i+5]),
                    vec2(v[i+6],v[i+7])
                    );
            indices.push_back(i/8);
            vertices.push_back(vertex);
        }



        mesh = Mesh(vertices,indices);
    }
};


